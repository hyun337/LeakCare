# 벡터 정규화(L2)
REGISTERED_EMBEDDING = USER_EMBEDDING_VECTOR / np.linalg.norm(USER_EMBEDDING_VECTOR)

async def analyze_face(image_bytes: bytes):
    """
    System이 보내준 바이너리 데이터를 받아 얼굴을 분석하고 유사도 점수를 반환
    """
    
    # 1. 바이너리 데이터를 넘파이 배열로 변환
    nparr = np.frombuffer(image_bytes, np.uint8)
    img = cv2.imdecode(nparr, cv2.IMREAD_COLOR)

    if img is None:
        return {"score": 0.0, "result": "error"}

    # 2. 얼굴 탐지 및 특징 추출
    faces = app.get(img)

    if len(faces) == 0:
        return {"face_count": 0, "results": []}

    # 3. 각 얼굴별로 임베딩 추출 + 유사도 비교
    results = []
    for i, face in enumerate(faces):
        if face.embedding is None:
            continue

        emb = face.embedding / np.linalg.norm(face.embedding)
        similarity_score = round(float(np.dot(REGISTERED_EMBEDDING, emb)), 4)

        results.append({
            "face_index": i,
            "score": similarity_score,
            "is_detected": similarity_score >= 0.85
        })

    return {"face_count": len(results), "results": results}


# 테스트용
import asyncio

async def main():
    with open("test_mult.png", "rb") as f:
        image_bytes = f.read()
    result = await analyze_face(image_bytes)
    print(result)

asyncio.run(main())
